package com.company;

public class MathMethods {
    public static void main(String[] args) {
        System.out.println(Math.max(23,78.9));
        System.out.println(Math.min(23,78.9));
        System.out.println(Math.sqrt(225));
        System.out.println(Math.ceil(233.89));
        System.out.println(Math.floor((23.89)));
        System.out.println(Math.abs(-2.344));
        System.out.println(Math.absExact(-200));
        System.out.println(Math.addExact(2, 44));
        System.out.println(Math.copySign(24, .45));
        System.out.println(Math.random());
    }
}
