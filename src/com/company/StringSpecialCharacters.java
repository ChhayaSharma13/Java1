package com.company;

public class StringSpecialCharacters {
    public static void main(String[] args) {
        System.out.println("Its a special \"String\" with few special \"characters\"");
        System.out.println("To show \\");
        System.out.println("To show \'");
        System.out.println("Hello, \n I'm Chhaya Sharma");
        System.out.println("Hello, \r I'm Chhaya Sharma");
        System.out.println("Hello, \t I'm Chhaya");
        System.out.println("Heya, \b I'm Chhaya");
        System.out.println("Hey, \f I'm Chhaya");
    }
}
